public class datatype {
    public static void main(String[] args) {
        // Data Types
        String name = "Rajan";
        int number = 100;
        char character = 'R';
        boolean bool = false;
        float d = 2255.55f;
        System.out.println(name);
        System.out.println(number);
        System.out.println(character);
        System.out.println(bool);
        System.out.println(d);

    }
    
}
