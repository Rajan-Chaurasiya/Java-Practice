public class assignment {
    public static void main(String[] args) {
        int x = 6;
        int y = 4;
        System.out.println(x+y);
        System.out.println(x-y);
        System.out.println(x/y);
        System.out.println(x%y);
        // x++ post increment
        // ++x pre increment
        y = x++;
        System.out.println(y);
    }
}
