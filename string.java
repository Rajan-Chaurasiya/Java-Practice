public class string {
    public static void main(String[] args) {
        String name = "abdhfgdgdfkgkh";
        System.out.println("The length of string is: " + name.length());
        System.out.println("The upper string is: " + name.toUpperCase());
        System.out.println("The index of g is: " + name.indexOf("g"));
    }
    
}
